(function() {
	'use strict';
	angular
		.module('scheduleSys')
		.factory('LicenseTypeService', LicenseTypeService);
	
	LicenseTypeService.$Inject = ['$resource'];
	
	function LicenseTypeService($resource) {
		var resourceUrl = '/license-types/:id';
		
		return $resource(resourceUrl, {}, {
            'query': { method: 'GET', isArray: true},
            'get': {
                method: 'GET',
                transformResponse: function (data) {
                    if (data) {
                        data = angular.fromJson(data);
                    }
                    return data;
                }
            },
            'update': { method:'PUT' },
            'remove':  {
                method: 'DELETE',
                transformResponse: function (data) {
                    if (data) {
                        data = angular.toJson(data);
                    }
                    return data;
                }
            },
            'save': {
                method: 'POST',
                transformResponse: function (data) {
                    if (data) {
                        data = angular.toJson(data);
                    }
                    return data;
                }
            }
        });
	}
})();